    private void hechoBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hechoBotonActionPerformed
        // TODO add your handling code here:
        if
                (!Sistema.esStringCorrecto(nombreInfo.getText()) ||
                !Sistema.esStringCorrecto(precioInfo.getText())  
                )
        {
            System.out.println("Falta alguna casilla por rellenar");        //Info para desarrolladores
        }
        else if(listaIngredientes.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Debe de añadir al menos 1 ingrediente", "Faltan ingredientes", JOptionPane.WARNING_MESSAGE);
        }
        else if(!Sistema.esImagenCorrecta(Sistema.getImagenActual()))
        {
            System.out.println("El usuario no selecciono ninguna imagen");  //Uso exclusivo de desarrolladores
        }
        else        //En esta sentencia tmb se comprueba si la comida existe con metodos de la clase Sistema.
        {
            //Si pasaron todos los filtros anteriores significa que se puede crear la comida
            Comida comida = new Comida(nombreInfo.getText(), listaIngredientes, precioInfo.getText(), Sistema.getImagenActual());
            //Guardo la comida en el archivo *.dat y ademas la cargo en la lista de comidas
            if(Sistema.anadirComida(comida))
            {
                Sistema.guardarDatosComidas();
                //Le confirmamo que la comida se ha creado
                JOptionPane.showMessageDialog(this, "Comida creada con exito", "INFO", JOptionPane.INFORMATION_MESSAGE);
                //Sale de la ventana
                ConsultaRestaurantes ventana = new ConsultaRestaurantes();
                ventana.setVisible(true);
                this.setVisible(false);
            }
            
            
        }
    }//GEN-LAST:event_hechoBotonActionPerformed

