    private void elegirImagenBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_elegirImagenBotonActionPerformed
        // TODO add your handling code here:
        try 
        {
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.addChoosableFileFilter(new ImageFilter());
            fileChooser.setAcceptAllFileFilterUsed(false);
            ImageIcon imagenFondo = new ImageIcon("Imagenes/fondoNegro.png");

            int option = fileChooser.showOpenDialog(this);
            if (option == JFileChooser.APPROVE_OPTION) 
            {
                file = fileChooser.getSelectedFile();
                rutaImagen = file.getAbsolutePath();
                nombreImagen = file.getName();
                String[] aux = nombreImagen.split("\\.");
                ImageIcon imagen = new ImageIcon(rutaImagen);
                ImageIcon imgRedimensionada = new ImageIcon(imagen.getImage().getScaledInstance(jLabelImagen.getWidth(), jLabelImagen.getHeight(), 1));
                jLabelImagen.setIcon(imgRedimensionada);
                Sistema.setImagenActual(nombreImagen); //Mando al sistema el nombre de la imagen, ejemplo: "imagen.png".
                //Guardo la imagen
                bimage = ImageIO.read(file);
                File fout = new File("./Imagenes/" + nombreImagen);
                ImageIO.write(bimage, extension, fout);

            } 
            else 
            {
                jLabelImagen.setIcon(imagenFondo);
                JOptionPane.showMessageDialog(this, "Ningúna imagen seleccinada", "Mensaje", JOptionPane.ERROR_MESSAGE);
                Sistema.setImagenActual("fondoNegro.png"); //Mando al sistema el nombre de la imagen, ejemplo: "imagen.png".

            }
        }
        catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al guardar imagen");
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(this, "Seleccione primero una imagen");
        }

    }//GEN-LAST:event_elegirImagenBotonActionPerformed

