    private void entrarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarBotonActionPerformed
        
        if(
                //Comprobamos que no haya ninguna casilla vacia
                !Sistema.esStringCorrecto(cifInfo.getText())                                    ||
                !Sistema.esStringCorrecto(nombreUsuarioInfo.getText())                          ||
                !Sistema.esStringCorrecto(String.valueOf(claveInfo.getPassword()))              ||
                !Sistema.esStringCorrecto(webInfo.getText())                                    ||
                !Sistema.esStringCorrecto(calleInfo.getText())                                  ||
                !Sistema.esStringCorrecto(numeroInfo.getText())                                 ||
                !Sistema.esStringCorrecto(String.valueOf(codigoPostalInfo.getText()))       ||
                !Sistema.esStringCorrecto(ciudadInfo.getText())                                 ||
                !Sistema.esStringCorrecto(nombreTitularInfo.getText())                          ||
                !Sistema.esStringCorrecto(numeroTarjetaInfo.getText())                          ||
                !Sistema.esStringCorrecto(fechaCaducidadInfo.getText())                                  
            
           )
        {
            //Info admin
            System.out.println("Falta alguna casilla por rellenar");      //Uso exlusivo para desarolladores
        }
        //Ahora comprobamos la fecha
        else if(!Sistema.esFechaCorrecta(fechaCaducidadInfo.getText()))
        {
            //Reiniciamos campo de texto
            fechaCaducidadInfo.setText("");
        }
        //Ahora si el correo es valido
        else if(!Sistema.esCorreoCorrecto(correoInfo.getText()))
        {
            //Reiniciamos campo de texto
            correoInfo.setText("");
        }
        else
        //Si el usuario pasa todas estas pruebas significa que puede realizar el registro
        {
            //Creamos la direccion
            Direccion direccion = new Direccion(calleInfo.getText(), numeroInfo.getText()
                    ,String.valueOf(codigoPostalInfo.getText()), ciudadInfo.getText());
            
            //Creamos la tarjeta de Credito
            TarjetaDeCredito tarjetaDeCredito = new TarjetaDeCredito(nombreTitularInfo.getText()
                                                            , numeroTarjetaInfo.getText()
                                                            , fechaCaducidadInfo.getText());
            
            //Creamos la empresa
            Empresa empresa = new Empresa(cifInfo.getText(), nombreUsuarioInfo.getText(), 
                                            correoInfo.getText(), String.valueOf(claveInfo.getPassword()),
                                            direccion, tarjetaDeCredito, telefonoInfo.getText(), webInfo.getText());
            
            //Procedemos con el registro
            if(Sistema.anadirEmpresa(empresa))      //Si la empresa esta ya registrada se comprueba en el metodo.
            {
            Sistema.guardarDatosEmpresas();
            JOptionPane.showMessageDialog(null, "Datos cambiados correctamente" , "INFO", JOptionPane.INFORMATION_MESSAGE);
            //Cerramos la ventana
            InicioSesion ventanaInicioSesion = new InicioSesion();
            ventanaInicioSesion.setVisible(true);
            this.setVisible(false);
            }
            else //Si entra aqui significa que la empresa ya estaba creada, le mandamos al Inicio de Sesion
            {
                //Cerramos la ventana
                InicioSesion ventanaInicioSesion = new InicioSesion();
                ventanaInicioSesion.setVisible(true);
                this.setVisible(false);
            }
        }
        
        

        
    }//GEN-LAST:event_entrarBotonActionPerformed

