    private void darDeBajaBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_darDeBajaBotonActionPerformed
        // TODO add your handling code here:
        
        int respuestaUsuario = JOptionPane.showConfirmDialog(this
                , "Seguro que deseas dar de baja tu cuenta"
                , "BORRADO DE CUENTA"
            ,JOptionPane.YES_NO_OPTION );
        
        if(respuestaUsuario == JOptionPane.YES_OPTION)
            {
            //Procedemos a eliminar el cliente de la base de datos
            String CIF = Sistema.getEmpresaActual().getCif();

            //Busco el cliente que se quiere eliminar
            int index = -1;
            for (Empresa empresa : Sistema.getListaEmpresas()) 
                {
                    index++;
                    if(empresa.getCif().equals(CIF))
                    {
                            Sistema.eliminarEmpresa(index);
                            break; //Para que no siga recorriendo la lista
                    }
                }
            //Guardo los datos
            Sistema.guardarDatosEmpresas();
            
            InicioSesion ventana = new InicioSesion();
            ventana.setVisible(true);
            this.setVisible(false);
            }
    }//GEN-LAST:event_darDeBajaBotonActionPerformed

