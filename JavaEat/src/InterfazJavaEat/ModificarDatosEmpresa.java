    public ModificarDatosEmpresa() {
        
        initComponents();
        this.setLocationRelativeTo(null);
        
        //Actualizamos los datos como siempre
        Sistema.cargarDatosClientes();
        Sistema.cargarDatosEmpresas();
        Sistema.cargarDatosRestaurantes();
        Sistema.cargarDatosComidas();
        Sistema.cargarDatosVentasClientes();
        Sistema.cargarDatosVentasEmpresas();
        Sistema.cargarDatosVentasCatering();
        
        //Establecemos todos los campos de texto con los datos del cliente
        cifInfo.setText(Sistema.getEmpresaActual().getCif());
        nombreUsuarioInfo.setText(Sistema.getEmpresaActual().getNombre());
        correoInfo.setText(Sistema.getEmpresaActual().getCorreoElectronico());
        claveInfo.setText(Sistema.getEmpresaActual().getClave());
        calleInfo.setText(Sistema.getEmpresaActual().getDireccion().getCalle());
        numeroInfo.setText(Sistema.getEmpresaActual().getDireccion().getNumeroCalle());
        codigoPostalInfo.setText(Sistema.getEmpresaActual().getDireccion().getCodigoPostal());
        ciudadInfo.setText(Sistema.getEmpresaActual().getDireccion().getCiudad());
        nombreTitularInfo.setText(Sistema.getEmpresaActual().getTarjetaDeCredito().getNombreTitular());
        numeroTarjetaInfo.setText(Sistema.getEmpresaActual().getTarjetaDeCredito().getNumeroDeTarjeta());
        fechaCaducidadInfo.setText(Sistema.getEmpresaActual().getTarjetaDeCredito().getFechaDeCaducidad());
        telefonoInfo.setText(Sistema.getEmpresaActual().getTelefono());
        webInfo.setText(Sistema.getEmpresaActual().getWeb());
        
        //Procedemos a eliminar la empresa de la base de datos
        String CIF = Sistema.getEmpresaActual().getCif();
        
        
        //Busco la empresa que se quiere eliminar
        int index = -1;
        for (Empresa empresa : Sistema.getListaEmpresas()) 
            {
                index++;
                if(empresa.getCif().equals(CIF))
                {
                        Sistema.eliminarEmpresa(index);
                        break; //Para que no siga recorriendo la lista
                }
            }
        
        
    }

